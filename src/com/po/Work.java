package com.po;


public class Work {
    private Integer id;

    private String workname;

	private String workkind;

    private String workcost;

    private String worksize;

    private Integer worknum;
    
    private Integer workpri;
    
    private String workcon;
    
    private Integer isvalid;
    
    private String worktempcon;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getWorkname() {
		return workname;
	}

	public void setWorkname(String workname) {
		this.workname = workname;
	}

	public String getWorkkind() {
		return workkind;
	}

	public void setWorkkind(String workkind) {
		this.workkind = workkind;
	}

	public String getWorkcost() {
		return workcost;
	}

	public void setWorkcost(String workcost) {
		this.workcost = workcost;
	}

	public String getWorksize() {
		return worksize;
	}

	public void setWorksize(String worksize) {
		this.worksize = worksize;
	}

	public Integer getWorknum() {
		return worknum;
	}

	public void setWorknum(Integer worknum) {
		this.worknum = worknum;
	}

	public Integer getWorkpri() {
		return workpri;
	}

	public void setWorkpri(Integer workpri) {
		this.workpri = workpri;
	}

	public String getWorkcon() {
		return workcon;
	}

	public void setWorkcon(String workcon) {
		this.workcon = workcon;
	}

	public Integer getIsvalid() {
		return isvalid;
	}

	public void setIsvalid(Integer isvalid) {
		this.isvalid = isvalid;
	}

	public String getWorktempcon() {
		return worktempcon;
	}

	public void setWorktempcon(String worktempcon) {
		this.worktempcon = worktempcon;
	}

   
}
